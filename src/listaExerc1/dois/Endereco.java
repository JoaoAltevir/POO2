package listaExerc1.dois;

public class Endereco {
	
	private String logradouro;
	private int numCasa;
	private String bairro;
	private String cep;
	private String cidade;
	private String uf;
	
	public Endereco(String logradouro, int numCasa, String bairro, String cep, String cidade, String uf) {
		super();
		this.logradouro = logradouro;
		this.numCasa = numCasa;
		this.bairro = bairro;
		this.cep = cep;
		this.cidade = cidade;
		this.uf = uf;
	}
	
	
}
